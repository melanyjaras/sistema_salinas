    public void beanView(Vendas vendas) {
        String valor = String.valueOf(vendas.getIdVenda());
        jTxtIdVenda.setText(valor);
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        jFmtDataVenda.setText(formato.format(vendas.getDataVenda()));
        jCboCliente.setSelectedIndex(vendas.getIdCliente());
        jCboVendedor.setSelectedIndex(vendas.getIdFuncionarios());
        jTxtTotal.setText(String.valueOf(vendas.getTotal()));

    }

    private Vendas viewBean() {
        Vendas vendas = new Vendas();
        int id = Integer.valueOf(jTxtIdVenda.getText());
        vendas.setIdVenda(id);
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        try {
            vendas.setDataVenda(formato.parse(jFmtDataVenda.getText()));
        } catch (ParseException ex) {

            //Logger.getLogger(jDlgVenda.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("Erro cabeÃ§udo:" + ex.getMessage());
        }
        Cliente clientes = (Cliente) jCboCliente.getSelectedItem();
        vendas.setIdCliente(clientes.getidCliente());

        Funcionarios funcionarios = (Funcionarios) jCboVendedor.getSelectedItem();
        vendas.setIdFuncionarios(funcionarios.getIdFuncionario());

        String textoTotal = jTxtTotal.getText();
        double total = Double.parseDouble(textoTotal);
        vendas.setTotal(total);

        return vendas;

    }

